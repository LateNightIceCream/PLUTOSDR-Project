function rx_param = QAMBitsReceiver(rx_param, signal)
    %% Rx-filter
    rx_param.pRxFilter = comm.RaisedCosineReceiveFilter( ...
    'RolloffFactor',            rx_param.RolloffFactor, ...
    'FilterSpanInSymbols',      rx_param.RaisedCosineFilterSpan, ...
    'InputSamplesPerSymbol',    rx_param.Interpolation, ...
    'DecimationFactor',         rx_param.Decimation);
    
    s = rx_param.pRxFilter(signal)
    scatterplot(s)
    
    rx_param.demod_symbols = qamdemod(s', rx_param.ModulationOrder, ...
                         UnitAveragePower=true, ...
                         PlotConstellation=true);

    %% Demodulate

    %% ...
end
